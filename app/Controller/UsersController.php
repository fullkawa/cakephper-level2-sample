<?php
App::uses('AppController', 'Controller');
/**
 * Users Controller
 *
 * @property User $User
 * @property PaginatorComponent $Paginator
 * @property SessionComponent $Session
 */
class UsersController extends AppController {

/**
 * Helpers
 *
 * @var array
 */
	public $helpers = array('Text');

/**
 * Components
 *
 * @var array
 */
	public $components = array('Paginator', 'Session', 'Payment');

/**
 * index method
 *
 * @return void
 */
	public function index() {
	    $this->helpers[] = 'UsersIndex';
		$this->User->recursive = 0;
		$this->set('registed_users', $this->User->getRegistedUsers());
		$this->set('users', $this->Paginator->paginate());
	}

/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->set('user', $this->User->findById($id));
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		}
	}

/**
 * edit method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function edit($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->request->is(array('post', 'put'))) {
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		} else {
			$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
			$this->request->data = $this->User->find('first', $options);
		}
	}

/**
 * delete method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function delete($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->request->onlyAllow('post', 'delete');
		if ($this->User->delete()) {
			$this->Session->setFlash(__('The user has been deleted.'));
		} else {
			$this->Session->setFlash(__('The user could not be deleted. Please, try again.'));
		}
		return $this->redirect(array('action' => 'index'));
	}

    /**
     * 月額課金で会員登録する
     */
    public function regist() {
        // チェック処理等は省略

        $options = array();
        $accountTimingKbn = Configure::read('ACCOUNT_TIMING_KBN');
        $options['accountTiming'] = Configure::read('ACCOUNT_TIMING');
        $firstAccountDate = date('Ymd');
        $firstAmount = $this->_getFirstAmount($this->Session->read('user'));
        $nextAmount = Configure::read('NEXT_AMOUNT');
        $options['commodity'] = Configure::read('COMMODITY');
        $memberAuthOkUrl = Configure::read('PAY_CERT_FOR_CONT_BILL_OK_URL');
        $memberAuthNgUrl = Configure::read('PAY_CERT_FOR_CONT_BILL_NG_URL');

        $result = $this->Payment->payCertForContBill($accountTimingKbn, $firstAccountDay, $firstAmount, $nextAmount, $memberAuthOkUrl,
            $memberAuthNgUrl, $options);

        return $result;
    }

    /**
     * ユーザの種別に応じた初回金額を取得する
     */
    public function _getFirstAmount($user) {
        $amount = Configure::read('FIRST_AMOUNT');

        /*
         * 招待ユーザ(type='invented')は初回無料
         */
        if ($user['type'] === 'invented') {
            $amount = 0;
        }
        return $amount;
    }

